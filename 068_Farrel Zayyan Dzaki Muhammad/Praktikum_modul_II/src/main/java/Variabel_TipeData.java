public class Variabel_TipeData {
    public static void main(String[] args ) {
        String s = "Belajar Java";
        char c = '0';
        int i = 12;
        float f = (float) 1.5;
        double d = 3.0;
        boolean b = true;
        
        // s, c, i, f, d, b, adalah nama VARIABEL
        /*
        String, char, int, float, double, boolean, adalah TIPE DATA
        */
        
        System.out.println("Tipe data String : " + s);
        System.out.println("Tipe data char :  " + c);
        System.out.println("Tipe data int : " + i);
        System.out.println("Tipe data float : " + f);
        System.out.println("Tipe data double : " + d);
        System.out.println("Tipe data boolean : " + b);
        
        
    }
}
